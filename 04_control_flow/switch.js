const month = "march"
switch (month){
    case "jan":
        console.log("month is january");
        break;                                    // shift +alt+down_arrow , after selecting a text
    case "feb":
        console.log("month is february");
        break;
    case "march":
        console.log("month is march");
        break;
    case "april":
        console.log("month is may");
        break;
    
    default :
        console.log("default case match");
        break
        

        
}



















